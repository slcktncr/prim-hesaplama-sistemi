    const recentSamples = await Sale.find({}).sort({ createdAt: -1 }).limit(5);
    console.log(`üìã Recent 5 sales timestamps:`);
    recentSamples.forEach((sale, idx) => {
      console.log(`  ${idx + 1}. ${sale.createdAt.toISOString()} (${sale.customerName})`);
    });
    
